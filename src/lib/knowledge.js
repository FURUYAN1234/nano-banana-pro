/**
 * 舞台に応じた具体的なディテール情報を取得するヘルパー関数
 */
export const getLocationDetails = (locationName) => {
  const details = locationDetails[locationName];
  if (details) {
    return `【環境設定データベース】\n・小道具(Props): ${details.props.join(", ")}\n・環境音: ${details.ambient}\n・視覚的特徴: ${details.visual}`;
  }
  
  // 登録されていない場所の場合は汎用の小道具をランダムにいくつか返す
  const shuffledProps = [...genericProps].sort(() => 0.5 - Math.random());
  return `【環境設定データベース】\n・小道具(Props): ${shuffledProps.slice(0, 3).join(", ")}\n※この場所ならではの具体的なディテールを積極的に描写せよ。`;
};

/**
 * ランダムに感情別のリアクションを抽出してプロンプトに追加するためのヘルパー
 */
export const getRandomReactions = () => {
  let reactionText = "【リアクション・データベース】\n";
  for (const [emotion, actions] of Object.entries(emotionReactions)) {
    const randomAction = actions[Math.floor(Math.random() * actions.length)];
    reactionText += `・${emotion}時: 「${randomAction}」などの物理的描写を使用\n`;
  }
  return reactionText;
};
