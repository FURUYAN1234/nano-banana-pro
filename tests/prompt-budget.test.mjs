test('Gemini manga prompt is not capped by the empirical ChatGPT Web-copy soft budget', () => {
  const prompt = buildMangaPrompt({
    scenario: buildLongGeminiBudgetStressScenario(),
    castList: LONG_GEMINI_CAST_LIST,
    colorMode: 'color',
    providerFamily: 'gemini',
    punchlineType: 'Auto',
    systemVersion: 'v4.8.2-test'
  });

  assert.ok(
    prompt.length > EMPIRICAL_CHATGPT_WEB_COPY_SOFT_BUDGET_CHARS,
    `expected Gemini prompt to be allowed beyond the empirical ChatGPT Web-copy soft budget (${EMPIRICAL_CHATGPT_WEB_COPY_SOFT_BUDGET_CHARS.toLocaleString()} chars), got ${prompt.length}`
  );
  assert.match(prompt, /Generated by Gemini with Super FURU AI 4-koma v4\.8\.2-test/);
  assert.doesNotMatch(prompt, /Generated by ChatGPT/);
  assert.match(prompt, /STRESS_MARKER_END_GEMINI_BUDGET/);
  assert.match(prompt, /PANEL STYLE LOCK: RETRO/);
  assert.match(prompt, /PANEL STYLE LOCK: POP_ART/);
  assert.match(prompt, /PANEL STYLE LOCK: WATERCOLOR/);
  assert.match(prompt, /PANEL STYLE LOCK: SKETCH/);
});
