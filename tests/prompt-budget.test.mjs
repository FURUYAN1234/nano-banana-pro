test('ChatGPT manga prompt stays within the empirical Web-copy soft budget without dropping critical content', () => {
  const prompt = buildMangaPrompt({
    scenario: LONG_FOUR_KOMA_SCENARIO,
    castList: FULL_CAST_LIST,
    colorMode: 'color',
    providerFamily: 'chatgpt',
    punchlineType: 'Documentary',
    systemVersion: 'v4.8.0'
  });

  assert.ok(
    prompt.length <= EMPIRICAL_CHATGPT_WEB_COPY_SOFT_BUDGET_CHARS,
    `expected ChatGPT prompt to stay within the empirical Web-copy soft budget (${EMPIRICAL_CHATGPT_WEB_COPY_SOFT_BUDGET_CHARS.toLocaleString()} chars), got ${prompt.length}`
  );
  assert.equal((prompt.match(/## Panel \d/g) || []).length, 4);
  assert.match(prompt, /35分長編映像に激辛評価!\?/);
  assert.match(prompt, /Generated by ChatGPT with Super FURU AI 4-koma v4\.8\.0/);
  assert.match(prompt, /Speech Bubble 1 \[ミク\]: "見てよ！35分フル実写長編できたんだけど！"/);
  assert.match(prompt, /Speech Bubble 1 \[サエコ\]: "ふむ、映像のつながりが破綻してる！"/);
  assert.doesNotMatch(prompt, /Speech Bubble \d \[リン\]: "90秒なら…"/);
  assert.doesNotMatch(prompt, /Speech Bubble \d \[リン\]: "35分は…"/);
  assert.doesNotMatch(prompt, /Speech Bubble \d: "今しかない！"/);
  assert.match(prompt, /Speech Bubble \d \[リン\]: "90秒なら我慢して見られるよ！"/);
  assert.match(prompt, /Speech Bubble \d \[アカリ\]: "35分は…とても最後まで見られない…"/);
  assert.match(prompt, /Speech Bubble \d \[ミク\]: "ひどいよー…。"/);
  assert.match(prompt, /Speech Bubble \d \[アカリ\]: "我々の仕事が奪われないうちにこの才能を潰そう！"/);
  assert.match(prompt, /Speech Bubble \d \[リン\]: "御意！"/);
  assert.match(prompt, /Speech Bubble \d \[サエコ\]: "AI作品は無条件で却下よね！"/);
  assert.match(prompt, /Dialogue \(verbatim bubbles\):/);
  assert.match(prompt, /vertical Japanese tategaki/i);
  assert.match(prompt, /softening/);
  assert.match(prompt, /synonyms/);
  assert.match(prompt, /MUST HAVE glasses/);
  assert.match(prompt, /MUST NOT have glasses/);
  assert.match(prompt, /THINGS TO AVOID:/);
  assert.match(prompt, /No plastic skin/);
  assert.match(prompt, /extra logos\/watermarks/);
  assert.match(prompt, /floating\/ghost eyes\/faces/);
  assert.match(prompt, /No sparkle\/glow dust/);
  assert.match(prompt, /HAND ANATOMY: correct hands/);
  assert.doesNotMatch(prompt, /NO quotes\/punctuation around title/);
});

